    END IF;

    SELECT COUNT(*)
      INTO v_count
      FROM USER_TAB_COLUMNS
     WHERE TABLE_NAME = 'FEE_POLICY_CONFIGS'
       AND COLUMN_NAME = 'TOSS_RATE';

    IF v_count = 0 THEN
        EXECUTE IMMEDIATE 'ALTER TABLE FEE_POLICY_CONFIGS ADD (TOSS_RATE NUMBER(10,4))';
    END IF;
