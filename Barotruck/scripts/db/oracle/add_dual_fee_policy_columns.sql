-- FEE_POLICY_CONFIGS dual-side fee policy columns
-- Safe to run multiple times.

DECLARE
    v_count NUMBER;
BEGIN
    SELECT COUNT(*)
      INTO v_count
      FROM USER_TAB_COLUMNS
     WHERE TABLE_NAME = 'FEE_POLICY_CONFIGS'
       AND COLUMN_NAME = 'SHIPPER_LEVEL0_RATE';

    IF v_count = 0 THEN
        EXECUTE IMMEDIATE 'ALTER TABLE FEE_POLICY_CONFIGS ADD (SHIPPER_LEVEL0_RATE NUMBER(10,4))';
    END IF;

    SELECT COUNT(*)
      INTO v_count
      FROM USER_TAB_COLUMNS
     WHERE TABLE_NAME = 'FEE_POLICY_CONFIGS'
       AND COLUMN_NAME = 'SHIPPER_LEVEL1_RATE';

    IF v_count = 0 THEN
        EXECUTE IMMEDIATE 'ALTER TABLE FEE_POLICY_CONFIGS ADD (SHIPPER_LEVEL1_RATE NUMBER(10,4))';
    END IF;

    SELECT COUNT(*)
      INTO v_count
      FROM USER_TAB_COLUMNS
     WHERE TABLE_NAME = 'FEE_POLICY_CONFIGS'
       AND COLUMN_NAME = 'SHIPPER_LEVEL2_RATE';

    IF v_count = 0 THEN
        EXECUTE IMMEDIATE 'ALTER TABLE FEE_POLICY_CONFIGS ADD (SHIPPER_LEVEL2_RATE NUMBER(10,4))';
    END IF;

    SELECT COUNT(*)
      INTO v_count
      FROM USER_TAB_COLUMNS
     WHERE TABLE_NAME = 'FEE_POLICY_CONFIGS'
       AND COLUMN_NAME = 'SHIPPER_LEVEL3_PLUS_RATE';

    IF v_count = 0 THEN
        EXECUTE IMMEDIATE 'ALTER TABLE FEE_POLICY_CONFIGS ADD (SHIPPER_LEVEL3_PLUS_RATE NUMBER(10,4))';
    END IF;

    SELECT COUNT(*)
      INTO v_count
      FROM USER_TAB_COLUMNS
     WHERE TABLE_NAME = 'FEE_POLICY_CONFIGS'
       AND COLUMN_NAME = 'DRIVER_LEVEL0_RATE';

    IF v_count = 0 THEN
        EXECUTE IMMEDIATE 'ALTER TABLE FEE_POLICY_CONFIGS ADD (DRIVER_LEVEL0_RATE NUMBER(10,4))';
    END IF;

    SELECT COUNT(*)
      INTO v_count
      FROM USER_TAB_COLUMNS
     WHERE TABLE_NAME = 'FEE_POLICY_CONFIGS'
       AND COLUMN_NAME = 'DRIVER_LEVEL1_RATE';

    IF v_count = 0 THEN
        EXECUTE IMMEDIATE 'ALTER TABLE FEE_POLICY_CONFIGS ADD (DRIVER_LEVEL1_RATE NUMBER(10,4))';
    END IF;

    SELECT COUNT(*)
      INTO v_count
      FROM USER_TAB_COLUMNS
     WHERE TABLE_NAME = 'FEE_POLICY_CONFIGS'
       AND COLUMN_NAME = 'DRIVER_LEVEL2_RATE';

    IF v_count = 0 THEN
        EXECUTE IMMEDIATE 'ALTER TABLE FEE_POLICY_CONFIGS ADD (DRIVER_LEVEL2_RATE NUMBER(10,4))';
    END IF;

    SELECT COUNT(*)
      INTO v_count
      FROM USER_TAB_COLUMNS
     WHERE TABLE_NAME = 'FEE_POLICY_CONFIGS'
       AND COLUMN_NAME = 'DRIVER_LEVEL3_PLUS_RATE';

    IF v_count = 0 THEN
        EXECUTE IMMEDIATE 'ALTER TABLE FEE_POLICY_CONFIGS ADD (DRIVER_LEVEL3_PLUS_RATE NUMBER(10,4))';
    END IF;

    SELECT COUNT(*)
      INTO v_count
      FROM USER_TAB_COLUMNS
     WHERE TABLE_NAME = 'FEE_POLICY_CONFIGS'
       AND COLUMN_NAME = 'SHIPPER_FIRST_PAYMENT_PROMO_RATE';

    IF v_count = 0 THEN
        EXECUTE IMMEDIATE 'ALTER TABLE FEE_POLICY_CONFIGS ADD (SHIPPER_FIRST_PAYMENT_PROMO_RATE NUMBER(10,4))';
    END IF;

    SELECT COUNT(*)
      INTO v_count
      FROM USER_TAB_COLUMNS
     WHERE TABLE_NAME = 'FEE_POLICY_CONFIGS'
